OUTPUT_FILE = "/mnt/d/fortune-data/books/zonghe/ziwei_doushu_verified.jsonl"

# Read existing entries
entries = []
seen_dedup = set()

if os.path.exists(OUTPUT_FILE):
    with open(OUTPUT_FILE, 'r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if line:
                e = json.loads(line)
                entries.append(e)
                seen_dedup.add(e["content"][:30])
    print(f"Loaded {len(entries)} existing entries")

def dedup(content):
    key = content[:30]
    if key in seen_dedup:
        return False
    seen_dedup.add(key)
    return True

def add(title, content, source_url="ziwei_knowledge_base", quality="authoritative"):
    if len(content) < 100:
        return False
    spam_kw = ["点击购买", "优惠活动", "限时特价", "立即注册", "添加微信"]
    for s in spam_kw:
        if s in content:
            return False
    if not dedup(content):
        return False
    entry = {
        "title": title, "content": content,
        "category": "ziwei", "source_url": source_url,
        "source_quality": quality, "verified": True
    }
    entries.append(entry)
    return True

def save():
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        for e in entries:
            f.write(json.dumps(e, ensure_ascii=False) + '\n')
    print(f"Saved {len(entries)} entries")

