def element_interact(e1, e2):
    if e1 == e2:
        return "比和"
    sheng = {"木": "火", "火": "土", "土": "金", "金": "水", "水": "木"}
    ke = {"木": "土", "土": "水", "水": "火", "火": "金", "金": "木"}
    if sheng.get(e1) == e2:
        return "相生"
    if ke.get(e1) == e2:
        return "相克"
    if sheng.get(e2) == e1:
        return "受生"
    return "平"


def calc_grids(surname, given_name):
    is_compound = surname in COMPOUND_SURNAMES
    if is_compound:
        s1, s2 = COMPOUND_SURNAMES[surname]
    else:
        s1, s2 = surname, None

    s1s = STROKES.get(s1, 1)
    s2s = STROKES.get(s2, 1) if s2 else None
    name_strokes = [STROKES.get(c, 1) for c in given_name]

    if is_compound:
        tiange = s1s + s2s
        rege = s2s + name_strokes[0]
        total = s1s + s2s + sum(name_strokes)
        waige = s1s + name_strokes[-1]
    else:
        tiange = s1s + 1
        rege = s1s + name_strokes[0]
        total = s1s + sum(name_strokes)
        waige = total - rege + 1

    dige = name_strokes[0] + 1 if len(name_strokes) == 1 else sum(name_strokes)

    return {
        "tiange": tiange, "rege": rege, "dige": dige,
        "zongge": total, "waige": waige,
        "tiange_i": get_num_info(tiange),
        "rege_i": get_num_info(rege),
        "dige_i": get_num_info(dige),
        "zongge_i": get_num_info(total),
        "waige_i": get_num_info(waige),
    }


