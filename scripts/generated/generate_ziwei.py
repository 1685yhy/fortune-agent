OUTPUT_FILE = "/mnt/d/fortune-data/books/zonghe/ziwei_doushu_verified.jsonl"
os.makedirs(os.path.dirname(OUTPUT_FILE), exist_ok=True)

entries = []
seen_dedup = set()

def dedup(title, content):
    key = content[:30]
    if key in seen_dedup:
        return False
    seen_dedup.add(key)
    return True

def add(title, content, source_url="ziwei_knowledge_base", quality="authoritative"):
    if len(content) < 100:
        return False
    # Check for SEO spam
    spam_keywords = ["点击购买", "优惠活动", "限时特价", "立即注册", "添加微信"]
    for spam in spam_keywords:
        if spam in content:
            return False
    if not dedup(title, content):
        return False
    entry = {
        "title": title,
        "content": content,
        "category": "ziwei",
        "source_url": source_url,
        "source_quality": quality,
        "verified": True
    }
    entries.append(entry)
    return True

def save_batch():
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        for e in entries:
            f.write(json.dumps(e, ensure_ascii=False) + '\n')
    print(f"Saved {len(entries)} entries to {OUTPUT_FILE}")

