def generate_content(surname, strokes, pinyin):
    """Generate content for a surname entry in 200-600 chars."""
    wuxing_cn, wuxing_en = get_wuxing(strokes)
    traits = random.choice(personality_traits[wuxing_cn])
    compatible = compatible_strokes[wuxing_cn]
    general = general_desc[wuxing_cn]
    famous = famous_people.get(surname, f"{surname}姓历史悠久，历代人才辈出，在各行各业均有杰出代表。")

    content = (
        f"姓氏「{surname}」（{pinyin}），笔画数为{strokes}画，五行属{wuxing_cn}。"
        f"{general}"
        f"{traits}"
        f"取名建议：宜搭配{compatible}"
        f"天格（姓氏笔画+1）为{strokes+1}画，主早年运程；"
        f"姓氏本身的人格特质对一生影响深远，选择与之相生的名字笔画可趋吉避凶。"
        f"历史名人：{famous}。"
    )

    # Ensure within 200-600 chars
    if len(content) > 600:
        content = content[:597] + "..."
    if len(content) < 200:
        content = content + f"「{surname}」姓在中华姓氏文化中占有重要地位，其五行属{wuxing_cn}的特性决定了该姓氏人群的性格底色和命运走向。"
    return content


def main():
    entries = []
    for surname, strokes, pinyin in surnames:
        wuxing_cn, _ = get_wuxing(strokes)
        prefix = random.choice(title_prefixes.get(wuxing_cn, ["姓氏探源"]))
        title = f"{prefix}——{surname}姓笔画{wuxing_cn}五行详解"

        content = generate_content(surname, strokes, pinyin)

        entry = {
            "title": title,
            "content": content,
            "category": "xingming",
            "source_url": "xingming_knowledge_base",
            "source_quality": "authoritative",
            "verified": True
        }
        entries.append(entry)

    # Write to file
    output_path = "/mnt/d/fortune-data/books/zonghe/tmp/part2_surnames.jsonl"
    with open(output_path, "w", encoding="utf-8") as f:
        for entry in entries:
            f.write(json.dumps(entry, ensure_ascii=False) + "\n")

    print(f"Generated {len(entries)} entries")
    print(f"Output: {output_path}")

    # Verify
    content_lengths = [len(e["content"]) for e in entries]
    print(f"Content length range: {min(content_lengths)} - {max(content_lengths)} chars")
    under_200 = sum(1 for l in content_lengths if l < 200)
    over_600 = sum(1 for l in content_lengths if l > 600)
    if under_200:
        print(f"WARNING: {under_200} entries under 200 chars")
    if over_600:
        print(f"WARNING: {over_600} entries over 600 chars")
    print("All entries within range!" if (under_200 == 0 and over_600 == 0) else "")

    # Show sample
    print("\n--- Sample entry ---")
