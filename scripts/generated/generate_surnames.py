def get_wuxing(strokes):
    """Determine five-element attribution based on stroke count."""
    remainder = strokes % 10
    if remainder in (1, 2):
        return "木", "wood"
    elif remainder in (3, 4):
        return "火", "fire"
    elif remainder in (5, 6):
        return "土", "earth"
    elif remainder in (7, 8):
        return "金", "metal"
    else:  # 9, 0
        return "水", "water"


