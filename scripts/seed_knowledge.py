def count_excerpts() -> int:
    """Count total number of excerpt entries across all categories."""
    total = 0
    for category, entries in EXCERPTS.items():
        total += len(entries)
    return total


def get_categories() -> list[str]:
    """Return list of available category names."""
    return list(EXCERPTS.keys())


def seed_vector_database(
    vectordb_dir: str | Path,
    embedding_model: str = "BAAI/bge-large-zh-v1.5",
    chunk_size: int = 500,
    chunk_overlap: int = 50,
    categories_filter: list[str] | None = None,
) -> int:
    """Embed all excerpts into the vector database.

    Returns total number of chunks added.
    """
    vectordb_dir = Path(vectordb_dir)
    vectordb_dir.mkdir(parents=True, exist_ok=True)

    print(f"Vector DB directory: {vectordb_dir}")
    print(f"Embedding model: {embedding_model}")
    print(f"Total excerpt entries: {count_excerpts()}")
    print()

    # Load embedder
    print("Loading embedder (first run will download BGE model)...")
    embedder = Embedder(model_name=embedding_model)

    print("Building vector index from seed knowledge...")
    retriever = Retriever(str(vectordb_dir), embedder)

    categories_to_process = categories_filter or get_categories()
    total_chunks = 0

    for category in categories_to_process:
        entries = EXCERPTS.get(category, [])
        if not entries:
            continue

        print(f"\n  Processing category: {category} ({len(entries)} entries)")
        for entry in tqdm(entries, desc=f"  [{category}]", unit="entry", leave=False):
            chunks = chunk_text(
                entry["text"],
                source=entry["source"],
                author=entry.get("author", ""),
                category=category,
                chunk_size=chunk_size,
                overlap=chunk_overlap,
            )
            if chunks:
                retriever.add_chunks(chunks)
                total_chunks += len(chunks)

    print(f"\nDone! Total chunks added: {total_chunks}")
    print(f"Collection size: {retriever.count()}")
    return total_chunks


def main() -> None:
    """Seed the vector database with classical text excerpts."""
    parser = argparse.ArgumentParser(
        description="Seed vector DB with built-in classical Chinese text excerpts.",
    )
    parser.add_argument(
        "--vectordb-dir",
        default=None,
        help="Path to vectordb directory (default: from settings)",
    )
    parser.add_argument(
        "--chunk-size",
        type=int,
        default=500,
        help="Max chunk size in characters (default: 500)",
    )
    parser.add_argument(
        "--overlap",
        type=int,
        default=50,
        help="Chunk overlap in characters (default: 50)",
    )
    parser.add_argument(
        "--categories",
        nargs="+",
        default=None,
        help="Categories to seed (default: all)",
    )
    parser.add_argument(
        "--list",
        action="store_true",
        help="List available categories and entry counts, then exit",
    )

    args = parser.parse_args()

    if args.list:
        print("Available categories and entry counts:")
        total = 0
        for cat in get_categories():
            count = len(EXCERPTS[cat])
            print(f"  {cat}: {count} entries")
            total += count
        print(f"  ─────────────────")
        print(f"  Total: {total} entries")
        return

    settings = load_settings()
    vdb_path = Path(args.vectordb_dir) if args.vectordb_dir else settings.vectordb_dir

    seed_vector_database(
        vectordb_dir=vdb_path,
        embedding_model=settings.embedding_model,
        chunk_size=args.chunk_size,
        chunk_overlap=args.overlap,
        categories_filter=args.categories,
    )


if __name__ == "__main__":
    main()
