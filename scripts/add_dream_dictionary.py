def generate_txt():
    """Generate the dream dictionary text file."""
    lines = []
    lines.append("周公解梦大全")
    lines.append("=" * 80)
    lines.append("")

    for category, cat_entries in entries.items():
        lines.append(f"【{category}】")
        lines.append("")
        for entry in cat_entries:
            lines.append(entry)
            lines.append("")
        lines.append("")

    txt = "\n".join(lines)
    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_FILE.write_text(txt, encoding="utf-8")
    logger.info("Written %d entries to %s", sum(len(v) for v in entries.values()), OUTPUT_FILE)
    return txt


def add_to_rag():
    """Chunk the dream dictionary and add to ChromaDB."""
    settings_path = Path(__file__).resolve().parent.parent / "config" / "settings.yaml"
    if not settings_path.exists():
        logger.warning("No settings.yaml found, using defaults")

    from src.config import load_settings
    settings = load_settings()

    text = OUTPUT_FILE.read_text(encoding="utf-8")

    # Chunk the text
    chunks = chunk_text(
        text, source="周公解梦大全", author="周公", category="dream",
        chunk_size=500, overlap=50,
    )
    logger.info("Created %d chunks from dream dictionary", len(chunks))

    # Check existing count
    embedder = Embedder(model_name=settings.embedding_model)
    retriever = Retriever(str(settings.vectordb_dir), embedder)
    before = retriever.count()
    logger.info("RAG count before: %d", before)

    # Filter out existing chunks
    existing_ids = set(retriever.collection.get()["ids"])
    new_chunks = [c for c in chunks if c.chunk_id not in existing_ids]
    logger.info("New chunks to add: %d (skipping %d existing)", len(new_chunks), len(chunks) - len(new_chunks))

    if new_chunks:
        # Add in batches
        retriever.add_chunks(new_chunks)
        after = retriever.count()
        logger.info("RAG count after: %d (added %d)", after, after - before)
    else:
        logger.info("All chunks already exist in RAG")
        after = before

    return after


