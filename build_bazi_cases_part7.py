def main():
    existing_titles = set()
    if VERIFIED_FILE.exists():
        with open(VERIFIED_FILE) as f:
            for line in f:
                line=line.strip()
                if line:
                    try: existing_titles.add(json.loads(line).get("title",""))
                    except: pass
    print(f"Existing: {len(existing_titles)}")
    new=0
    with open(VERIFIED_FILE,"a") as f:
        for c in CASES:
            if len(c.get("content",""))<100: continue
            if c["title"] in existing_titles: continue
            f.write(json.dumps(c,ensure_ascii=False)+"\n")
            existing_titles.add(c["title"])
            new+=1
    print(f"New: {new}, Total: {len(existing_titles)}")

if __name__=="__main__":
    main()
