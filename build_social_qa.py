#!/usr/bin/env python3
"""
Build social_qa.jsonl from web-search-sourced Chinese fortune-telling Q&A data.
Sources: Zhihu, Douban, Tieba, Xiaohongshu, 17house, and other Chinese platforms.

Each line is a JSON object with fields:
  - question:  str  — original user question
  - answer:    str  — top answer / reply (empty if none)
  - source:    str  — platform URL or name
  - category:  str  — bazi / dream / fengshui / mianxiang / palm / divination / general
  - subcategory: str — more specific
  - platform:  str  — zhihu / douban / tieba / xiaohongshu / 17house / other
"""

import json
import os

OUTPUT = "/mnt/d/fortune-data/books/zonghe/social_qa.jsonl"
CHUNK = 20   # save every N entries

entries = []
SAVED = 0

def save(force=False):
    global SAVED
    if not force and len(entries) - SAVED < CHUNK:
        return
    with open(OUTPUT, "a", encoding="utf-8") as f:
        for e in entries[SAVED:]:
            f.write(json.dumps(e, ensure_ascii=False) + "\n")
    SAVED = len(entries)
    print(f"  Saved {SAVED}/{len(entries)} entries")

def add(q, a, source, cat, subcat, platform):
    entries.append(dict(
        question=q.strip(),
        answer=a.strip() if a else "",
        source=source,
        category=cat,
        subcategory=subcat,
        platform=platform,
    ))
    save()

