# 五行元素列表
_WUXING_ELEMENTS = {"金", "木", "水", "火", "土"}
# 显示用的5大领域列表
ALL_CATEGORIES = ["事业", "财运", "感情", "健康", "个人成长"]


class AdvisorEngine:
    """行动建议引擎 —— 基于八字结果生成个性化、可执行的建议。"""

    def __init__(self):
        pass

    def generate_advice(self, result) -> Dict[str, List[str]]:
        """生成5大领域的行动建议。

        Args:
            result: BaziResult 对象（或兼容的 data class）

        Returns:
            dict: {category: [advice_strings], ...} 五大领域
        """
        try:
            advice: Dict[str, List[str]] = {cat: [] for cat in ALL_CATEGORIES}
            self._add_yongshen_advice(result, advice)
            self._add_geju_advice(result, advice)
            self._add_wuxing_advice(result, advice)
            self._add_shensha_advice(result, advice)
            return advice
        except Exception:
            return {cat: [] for cat in ALL_CATEGORIES}

    # -----------------------------------------------------------
    # 1. 用神-based advice
    # -----------------------------------------------------------

    def _parse_yongshen(self, yongshen_str: str) -> Dict[str, str]:
        """解析用神字符串。

        "水为用神（调候优先）（喜水、木）" → {"main": "水", "xi": ["水", "木"]}
        """
        result: Dict[str, list] = {"main": "", "xi": []}
        if not isinstance(yongshen_str, str):
            return result
        m = re.match(r"([金木水火土])为用神", yongshen_str)
        if m:
            result["main"] = m.group(1)
        m = re.search(r"喜([金木水火土、]+)", yongshen_str)
        if m:
            parts = re.split(r"[、]", m.group(1).strip())
            result["xi"] = [p for p in parts if p in _WUXING_ELEMENTS]
        return result

    def _add_yongshen_advice(self, result, advice: Dict[str, List[str]]) -> None:
        yongshen = getattr(result, "yongshen", "")
        parsed = self._parse_yongshen(yongshen)
        elements = []
        if parsed["main"]:
            elements.append(parsed["main"])
        elements.extend(parsed["xi"])
        # deduplicate preserve order
        seen: set = set()
        for elem in elements:
            if elem and elem not in seen:
                seen.add(elem)
                if elem in YONGSHEN_ADVICE:
                    for cat in ALL_CATEGORIES:
                        items = YONGSHEN_ADVICE[elem].get(cat, [])
                        advice[cat].extend(items)

    # -----------------------------------------------------------
    # 2. 格局-based advice
    # -----------------------------------------------------------

    def _add_geju_advice(self, result, advice: Dict[str, List[str]]) -> None:
        geju = getattr(result, "geju", "")
        if not isinstance(geju, str) or not geju:
            return
        geju_data = GEJU_ADVICE.get(geju, {})
        for cat, items in geju_data.items():
            if cat in advice:
                advice[cat].extend(items)

    # -----------------------------------------------------------
    # 3. 五行平衡 advice
    # -----------------------------------------------------------

    def _add_wuxing_advice(self, result, advice: Dict[str, List[str]]) -> None:
        wuxing = getattr(result, "wuxing", None)
        if not isinstance(wuxing, dict):
            return

        # 五行缺 → 补充建议 → 个人成长
        for wx in _WUXING_ELEMENTS:
            if wuxing.get(wx, 0) == 0:
                items = WUXING_QUE.get(wx, [])
                for item in items:
                    advice["个人成长"].append(f"五{_element_emoji(wx)}行缺{wx}：{item}")

        # 五行过旺 → 平衡建议 → 个人成长
        for wx in _WUXING_ELEMENTS:
            if wuxing.get(wx, 0) >= 3:
                items = WUXING_GUO.get(wx, [])
                for item in items:
                    advice["个人成长"].append(f"五{_element_emoji(wx)}行{wx}过旺：{item}")

        # 五行过旺 → 健康提示
        wuxing_health = {
            "金": ["金旺注意呼吸系统、肺部健康", "金克木，注意肝胆调理"],
            "木": ["木旺注意肝胆、神经系统", "木克土，注意脾胃调理"],
            "水": ["水旺注意肾脏、泌尿系统", "水克火，注意心血管调理"],
            "火": ["火旺注意心脏、血液循环", "火克金，注意肺部调理"],
            "土": ["土旺注意脾胃、消化系统", "土克水，注意肾脏调理"],
        }
        for wx, health_tips in wuxing_health.items():
            if wuxing.get(wx, 0) >= 3:
                for tip in health_tips:
                    advice["健康"].append(tip)

    # -----------------------------------------------------------
    # 4. 神煞 advice
    # -----------------------------------------------------------

    def _add_shensha_advice(self, result, advice: Dict[str, List[str]]) -> None:
        shensha = getattr(result, "shensha", None)
        if not isinstance(shensha, (list, tuple)):
            return
        for ss in shensha:
            if isinstance(ss, str) and ss in SHENSHA_ADVICE:
                advice["个人成长"].extend(SHENSHA_ADVICE[ss])


def _element_emoji(wx: str) -> str:
    """五行对应的简短 emoji/indicator."""
    return {"金": "⚪", "木": "🟢", "水": "🔵", "火": "🔴", "土": "🟡"}.get(wx, "")
