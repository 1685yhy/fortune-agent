# ============================================================
# 五行 utilities
# ============================================================

def wuxing_generates(wx: str) -> str:
    """Return the element generated by wx. 木→火→土→金→水→木"""
    cycle = {"木": "火", "火": "土", "土": "金", "金": "水", "水": "木"}
    return cycle[wx]


def wuxing_controls(wx: str) -> str:
    """Return the element controlled by wx. 木→土→水→火→金→木"""
    cycle = {"木": "土", "土": "水", "水": "火", "火": "金", "金": "木"}
    return cycle[wx]


def calc_liuqin(palace_wuxing: str, line_wuxing: str) -> str:
    """Calculate the 六亲 relationship between a palace and a line.

    Relationships:
      - 同我 (same element)       -> 兄弟
      - 我生 (palace generates)   -> 子孙
      - 生我 (line generates)     -> 父母
      - 我克 (palace controls)    -> 妻财
      - 克我 (line controls)      -> 官鬼
    """
    if palace_wuxing == line_wuxing:
        return "兄弟"
    if wuxing_generates(palace_wuxing) == line_wuxing:
        return "子孙"
    if wuxing_generates(line_wuxing) == palace_wuxing:
        return "父母"
    if wuxing_controls(palace_wuxing) == line_wuxing:
        return "妻财"
    if wuxing_controls(line_wuxing) == palace_wuxing:
        return "官鬼"
    return "兄弟"


# ============================================================
# Data model
# ============================================================

@dataclass
class LiuyaoResult:
    """六爻占卜结果

    Attributes:
        original_hexagram: 本卦卦名, e.g. "天地否"
        changed_hexagram: 变卦卦名, e.g. "风地观" (empty string if no changing lines)
        lines: 6爻详情, each a dict with keys:
            - type: "老阴"|"少阳"|"少阴"|"老阳"
            - yao_type: "世"|"应"|""
            - liuqin: "兄弟"|"子孙"|"父母"|"妻财"|"官鬼"
            - dizhi: 地支
        shi_yao: 世爻位置 (0-5)
        ying_yao: 应爻位置 (0-5)
        changing_lines: 动爻索引, e.g. [2, 4] (empty if 静卦)
        palace: 所属宫名, e.g. "乾"
        palace_wuxing: 宫五行, e.g. "金"
        original_hexagram_value: 本卦6-bit数值 (binary: 上卦|下卦)
        changed_hexagram_value: 变卦6-bit数值
        question: 所问之事
    """
    original_hexagram: str
    changed_hexagram: str
    lines: List[Dict[str, str]]
    shi_yao: int
    ying_yao: int
    changing_lines: List[int]
    palace: str
    palace_wuxing: str
    original_hexagram_value: int
    changed_hexagram_value: int
    question: str = ""
    raw_data: dict = field(default_factory=dict)


# ============================================================
# Liuyao Engine
# ============================================================

class LiuyaoEngine:
    """六爻占卜引擎

    Usage:
        engine = LiuyaoEngine()
        result = engine.cast(method="random", question="财运", seed=42)
    """

    def cast(self, method: str = "random", question: str = "",
             seed: Optional[int] = None) -> LiuyaoResult:
        """起卦占卜

        Args:
            method: 起卦方法. "random" 模拟三枚硬币抛六次.
            question: 所问之事 (optional).
            seed: 随机种子, 用于可重复测试.

        Returns:
            LiuyaoResult containing the complete divination result.
        """
        if method == "random":
            return self._cast_coins(question, seed)
        raise ValueError(f"Unsupported method: {method}. Supported: 'random'.")

    def lookup_hexagram(self, upper: str, lower: str) -> Optional[Tuple[str, int, int]]:
        """Look up a hexagram by its upper and lower trigram names.

        Args:
            upper: Upper trigram name (乾兑离震巽坎艮坤).
            lower: Lower trigram name.

        Returns:
            (name, palace_index, shi_position) or None if not found.
        """
        u_val = TRIGRAM_BY_NAME.get(upper)
        l_val = TRIGRAM_BY_NAME.get(lower)
        if u_val is None or l_val is None:
            return None
        value = (u_val << 3) | l_val
        return HEXAGRAM_TABLE.get(value)

    def get_line_dizhi(self, upper: str, lower: str) -> List[str]:
        """Calculate the 纳甲 (Najia) earthly branch for each of the 6 lines.

        Returns:
            List of 6 地支, from line 0 (初爻) to line 5 (上爻).
        """
        u_lower_start, u_upper_start, u_is_yang = TRIGRAM_NAJIA[upper]
        l_lower_start, _, l_is_yang = TRIGRAM_NAJIA[lower]

        dizhi: List[str] = []
        for i in range(6):
            if i < 3:
                # Lower trigram lines (初, 二, 三)
                if l_is_yang:
                    dz_idx = (l_lower_start + i * 2) % 12
                else:
                    dz_idx = (l_lower_start - i * 2) % 12
            else:
                # Upper trigram lines (四, 五, 上)
                if u_is_yang:
                    dz_idx = (u_upper_start + (i - 3) * 2) % 12
                else:
                    dz_idx = (u_upper_start - (i - 3) * 2) % 12
            dizhi.append(DIZHI[dz_idx])
        return dizhi

    def _cast_coins(self, question: str = "",
                    seed: Optional[int] = None) -> LiuyaoResult:
        """Simulate tossing 3 coins 6 times to generate a hexagram.

        Each coin: heads (阳) = 2, tails (阴) = 3.
        Sum values:
            6 = 老阴 (changing yin)
            7 = 少阳 (young yang)
            8 = 少阴 (young yin)
            9 = 老阳 (changing yang)
        """
        rng = random.Random(seed)

        raw_lines: List[Dict] = []
        hex_value = 0  # Build 6-bit hexagram from bottom (LSB)

        for i in range(6):
            total = rng.choice([2, 3]) + rng.choice([2, 3]) + rng.choice([2, 3])

            if total == 6:
                line_type = "老阴"
                yin_yang = 0
            elif total == 7:
                line_type = "少阳"
                yin_yang = 1
            elif total == 8:
                line_type = "少阴"
                yin_yang = 0
            elif total == 9:
                line_type = "老阳"
                yin_yang = 1
            else:
                raise RuntimeError(f"Unexpected coin sum: {total}")

            if yin_yang:
                hex_value |= (1 << i)

            raw_lines.append({
                "index": i,
                "type": line_type,
                "yin_yang": yin_yang,
                "changing": line_type in ("老阴", "老阳"),
                "value": total,
            })

        # Lookup hexagram
        entry = HEXAGRAM_TABLE[hex_value]
        hex_name, palace_idx, shi_pos = entry

        ying_pos = (shi_pos + 3) % 6

        # Line 地支
        upper_val = (hex_value >> 3) & 0b111
        lower_val = hex_value & 0b111
        upper_name = TRIGRAM_NAMES[upper_val]
        lower_name = TRIGRAM_NAMES[lower_val]
        line_dizhi = self.get_line_dizhi(upper_name, lower_name)

        # Build detailed lines with 六亲 and 世应
        palace_wx = PALACE_WUXING[palace_idx]

        full_lines: List[Dict] = []
        changing_lines: List[int] = []

        for i, rl in enumerate(raw_lines):
            dz = line_dizhi[i]
            dz_wx = DIZHI_WUXING[dz]
            liuqin = calc_liuqin(palace_wx, dz_wx)

            if i == shi_pos:
                yao_type = "世"
            elif i == ying_pos:
                yao_type = "应"
            else:
                yao_type = ""

            full_lines.append({
                "type": rl["type"],
                "yao_type": yao_type,
                "liuqin": liuqin,
                "dizhi": dz,
            })

            if rl["changing"]:
                changing_lines.append(i)

        # Calculate 变卦 (flip changing lines)
        changed_value = hex_value
        for i in changing_lines:
            changed_value ^= (1 << i)

        changed_name = ""
        if changing_lines:
            changed_entry = HEXAGRAM_TABLE.get(changed_value)
            if changed_entry:
                changed_name = changed_entry[0]
        else:
            changed_name = hex_name  # 静卦: same as original

        return LiuyaoResult(
            original_hexagram=hex_name,
            changed_hexagram=changed_name,
            lines=full_lines,
            shi_yao=shi_pos,
            ying_yao=ying_pos,
            changing_lines=changing_lines,
            palace=TRIGRAM_NAMES[palace_idx],
            palace_wuxing=palace_wx,
            original_hexagram_value=hex_value,
            changed_hexagram_value=changed_value,
            question=question,
            raw_data={
                "raw_lines": raw_lines,
                "line_dizhi": line_dizhi,
                "upper_trigram": upper_name,
                "lower_trigram": lower_name,
            },
        )
