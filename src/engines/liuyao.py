"""六爻占卜引擎 - Liuyao (I Ching) divination via coin toss simulation.

Core concepts:
  - 6 lines (爻) generated from bottom (初) to top (上)
  - 3 coins per line: sum 6=老阴(changing), 7=少阳, 8=少阴, 9=老阳(changing)
  - 本卦 (original hexagram) built from all 6 lines
  - 变卦 (changed hexagram) = 本卦 with changing lines flipped
  - 宫 (palace, 八宫): 乾坎艮震巽离坤兑, each with a 五行
  - 世应: host/guest line positions
  - 六亲: 6 relationships based on wuxing interactions
"""

