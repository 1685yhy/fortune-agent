def main():
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    existing_titles = set()
    if VERIFIED_FILE.exists():
        with open(VERIFIED_FILE) as f:
            for line in f:
                line = line.strip()
                if line:
                    try:
                        existing_titles.add(json.loads(line).get("title",""))
                    except: pass

    print(f"Existing: {len(existing_titles)}")
    new_count = 0

    with open(VERIFIED_FILE, "a") as vf:
        for case in CASES:
            if len(case.get("content","")) < 100: continue
            if case["title"] in existing_titles: continue
            vf.write(json.dumps(case, ensure_ascii=False) + "\n")
            existing_titles.add(case["title"])
            new_count += 1

    print(f"New: {new_count}, Total: {len(existing_titles)}")

if __name__ == "__main__":
    main()
